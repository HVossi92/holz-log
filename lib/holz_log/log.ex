defmodule HolzLog.Log do
  @moduledoc """
  The Log context.
  """

  import Ecto.Query, warn: false
  alias HolzLog.Repo

  alias HolzLog.Log.Note

  @doc """
  Returns the list of notes.

  ## Examples

      iex> list_notes()
      [%Note{}, ...]

  """
  def list_notes(search \\ nil, category_id \\ nil) do
    IO.puts("list_notes")

    search_pattern = "%#{search}%"
    IO.inspect(category_id)

    base_query =
      Note
      |> join(:left, [n], c in assoc(n, :categories))
      |> preload([:categories])
      |> where(
        [n, c],
        fragment("? LIKE ? COLLATE NOCASE", n.title, ^search_pattern) or
          fragment("? LIKE ? COLLATE NOCASE", n.body, ^search_pattern) or
          fragment("? LIKE ? COLLATE NOCASE", c.title, ^search_pattern)
      )

    # Only add category filter if category_id is provided and not nil
    if category_id !== nil and category_id > -1 do
      base_query
      |> where([n, c], c.id == ^category_id)
    else
      base_query
    end
    |> Repo.all()
  end

  @doc """
  Gets a single note.

  Raises `Ecto.NoResultsError` if the Note does not exist.

  ## Examples

      iex> get_note!(123)
      %Note{}

      iex> get_note!(456)
      ** (Ecto.NoResultsError)

  """
  def get_note!(id), do: Note |> Repo.get!(id) |> Repo.preload(:categories)

  @doc """
  Creates a note.

  ## Examples

      iex> create_note(%{field: value})
      {:ok, %Note{}}

      iex> create_note(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_note(attrs \\ %{}) do
    %Note{}
    |> Note.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a note.

  ## Examples

      iex> update_note(note, %{field: new_value})
      {:ok, %Note{}}

      iex> update_note(note, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_note(%Note{} = note, attrs) do
    note
    |> Note.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a note.

  ## Examples

      iex> delete_note(note)
      {:ok, %Note{}}

      iex> delete_note(note)
      {:error, %Ecto.Changeset{}}

  """
  def delete_note(%Note{} = note) do
    Repo.delete(note)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking note changes.

  ## Examples

      iex> change_note(note)
      %Ecto.Changeset{data: %Note{}}

  """
  def change_note(%Note{} = note, attrs \\ %{}) do
    Note.changeset(note, attrs)
  end

